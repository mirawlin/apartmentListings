class Apartment {
    constructor({ id, name, description, address, city, postcode, energyRating }) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.address = address;
        this.city = city;
        this.postcode = postcode;
        this.energyRating = energyRating;
    }
}

export { Apartment }
